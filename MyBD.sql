create table bell (id int PRIMARY KEY, hours time);
create table corpus (id int PRIMARY KEY, corpus int);
create table dayofweek (id int PRIMARY KEY, dayofweek text);
create table faculty (id int PRIMARY KEY, faculty text);
create table teachers (id int PRIMARY KEY, fio text);
create table disc(id int PRIMARY KEY, name_disc text);
create table groupa (id int PRIMARY KEY, group_name text, id_faculty int, FOREIGN KEY (id_faculty) REFERENCES faculty (id));
create table audit (id int PRIMARY KEY, number text, id_corpus int, FOREIGN KEY (id_corpus) REFERENCES corpus (id));
create table raspisanie (
	id int PRIMARY KEY,  
	id_group int, 
	id_dow int, 
	id_time int, 
	id_disc int, 
	id_teacher int, 
	id_audit int,
	FOREIGN KEY (id_group) REFERENCES groupa (id),
	FOREIGN KEY (id_dow) REFERENCES dayofweek  (id),
	FOREIGN KEY (id_time) REFERENCES bell (id),
	FOREIGN KEY (id_disc) REFERENCES disc (id),
	FOREIGN KEY (id_teacher) REFERENCES teachers (id),
	FOREIGN KEY (id_audit) REFERENCES audit (id)
);

